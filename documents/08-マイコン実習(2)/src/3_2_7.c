int main(void) {
  init_MTLED();

  char char_set[] = {0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x1f, 0x24, 0x44, 0x24,
                     0x1f, 0x00, 0x7f, 0x08, 0x14, 0x22, 0x41, 0x00, 0x7f, 0x49,
                     0x49, 0x49, 0x36, 0x00, 0x0c, 0x14, 0x24, 0x7f, 0x04, 0x00,
                     0x36, 0x49, 0x49, 0x49, 0x36, 0x00, 0x21, 0x7f, 0x01, 0x00};

  int offset = 0;

  while (1) {
    for (int i = 0; i < 8; i++) {
      int address = i + 1;
      int left = i + offset;
      int right = left + 8;

      send_dual(address, char_set[left % 40], address, char_set[right % 40]);
    }
    delayms(1000);
    offset++;
  }
}
